/**
 * Get price for a drug. Uses DRUG_PRICES map or random 0-200 for unknown.
 */
export function getDrugPrice(drugName: string): number {
  const trimmed = drugName.trim();
  if (!trimmed) return Math.floor(Math.random() * 201);
  const lower = trimmed.toLowerCase();
  for (const [name, price] of Object.entries(DRUG_PRICES)) {
    if (name.toLowerCase() === lower) return price;
  }
  return Math.floor(Math.random() * 201);
}

/**
 * Filter drugs by search query (Arabic/English, partial match)
 */
export function searchDrugs(query: string, limit = 10): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];

  return DRUG_NAMES.filter((name) =>
    name.toLowerCase().includes(q)
  ).slice(0, limit);
}
